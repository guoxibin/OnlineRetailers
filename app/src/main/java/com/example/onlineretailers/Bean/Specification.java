package com.example.onlineretailers.Bean;

public class Specification {
    private String size;
    private String weight;
    private String other_specifications;
    public void setSize(String size) {
        this.size = size;
    }
    public String getSize() {
        return size;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
    public String getWeight() {
        return weight;
    }

    public void setOther_specifications(String other_specifications) {
        this.other_specifications = other_specifications;
    }
    public String getOther_specifications() {
        return other_specifications;
    }
}
